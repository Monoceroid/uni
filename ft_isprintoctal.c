int	ft_isprint(int c)
{
	if ((c > 39 && c < 48) || (c > 49 && c < 58) || (c > 59 && c < 68)
			|| (c > 69 && c < 78) || (c > 99 && c < 108) || (c > 109
				&& c < 118) || (c > 119 && c < 128) || (c > 129 && c
					< 138) || (c > 139 && c < 148) || (c > 149 && c
						< 158) || (c > 159 && c < 168) || (c > 169 && c < 177))
		return (1);
	else
		return (0);
}
